const a = 7
let b = 3 

b += a // b = b + a, faz a soma direto
console.log(b)

b -= 4 // b = b - 4, faz a subtração direto
console.log(b)

b *= 2 // b = b * 2, faz a multiplicação direto
console.log(b)

b /= 2 // b = b / 2, faz a divisão direto
console.log(b)

b %= 2 // b = b % 2, devolve o resto da divisão
console.log(b)

b **= 3 // b = b ** 3, faz a exponenciação direto